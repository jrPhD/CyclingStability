    def formulate_Opty_problem(self, MAX_INTER = 3000):
        """
        Formulate the problem with Opty.

        Parameters
        ----------
        MAX_INTER : Int, optional
            Maximum number of iteration of the solver. The default is 3000.

        Returns
        -------
        None.

        """
        
        INTERVAL_VALUE = self.INTERVAL_VALUE
        NUM_STATES = self.NUM_STATES
        NUM_INPUTS = self.NUM_INPUTS
        NUM_NODES = self.NUM_NODES

        def obj(free):
            """Cost function definition"""
            x, r, _ = parse_free(free, NUM_STATES, NUM_INPUTS, NUM_NODES)
    
            q2 = x[1]
            
            #Minimize latral travelled distance
            J2 = INTERVAL_VALUE*((WEIGHT)*(np.sum(q2)**2))
        
            J3 = INTERVAL_VALUE*((1-WEIGHT)*(np.sum((r.flatten())**2)))
            
            return J2+J3
    
    
        def obj_grad(free):
            x, r, _ = parse_free(free, NUM_STATES, NUM_INPUTS, NUM_NODES)
    
            q2 = x[1]
        
            grad = np.zeros_like(free)
            
            grad[1*NUM_NODES:2*NUM_NODES] = 2.0*INTERVAL_VALUE*WEIGHT*q2
            grad[NUM_STATES*NUM_NODES:(NUM_STATES + NUM_INPUTS)*NUM_NODES] = 2.0*(1.0-WEIGHT)*INTERVAL_VALUE*r.flatten()
            
            return grad
        
        motor_noise_magnitude = 2.5
        
        time_2 = time.time()
        
        instance_constraints = tuple(
                xi.replace(self.t, 0.0) - xi_val for xi, xi_val in self.initial_state_constraints.items()) + tuple(
                xi.replace(self.t, self.DURATION) - xi_val for xi, xi_val in self.final_state_constraints.items())

        self.known_trajectories = {dist : np.random.normal(0, motor_noise_magnitude, self.NUM_NODES) for dist in self.disturbance}

        state_vars = self.x.col_join(self.r)

        self.problem = Problem(
            obj,
            obj_grad,
            self.eoms,
            state_vars,
            self.NUM_NODES,
            self.INTERVAL_VALUE,
            known_parameter_map = self.p,
            known_trajectory_map = self.known_trajectories,
            instance_constraints= instance_constraints,
            bounds= self.bounds,
            # integration_method='midpoint',
            time_symbol= self.t,
            parallel= True,
            # backend='numpy'
        )
        self.problem.add_option('max_iter' , MAX_INTER)
        
        time_3 = time.time()
        self.prob_def_time = round(time_3 - time_2, 2)
        
        print(f'-OPTY PROBLEM DEFINED - {self.prob_def_time} sec')
        
        
    def solve_Opty_problem(self, initial_guess, visualization_flag = True, N_start=1):
        """
        Run the solver, save and plot the solution found.
        A multi-start feature is implemented. All the results are solved and plot.

        Parameters
        ----------
        initial_guess : np.array ((NUM_STATES_TOT+NUM_INPUTS_TOT)*NUM_NODES,)
            initial guess given to Opty solver.
        visualization_flag : bool, optional
            if True, plots are made. The default is True.
        N_start : int, optional
            Number of start used for the multi-start approach. The default is 1.

        Returns
        -------
        None.

        """
        

        path_results_folder = make_new_results_folder('results/')
        self.t_simu = np.linspace(0, self.DURATION, self.NUM_NODES)
        initial_guess_len = (self.NUM_STATES_TOT+self.NUM_INPUTS_TOT)*self.NUM_NODES
        
        
        if len(initial_guess) != initial_guess_len :
            
            new_initial_guess = np.empty(initial_guess_len)
            previous_NUM_NODES = len(initial_guess) // (self.NUM_STATES_TOT+self.NUM_INPUTS_TOT)
            
            for k in range(self.NUM_STATES_TOT+self.NUM_INPUTS_TOT):
                x_k = initial_guess[k*previous_NUM_NODES:(k+1)*previous_NUM_NODES]
                t_interp = np.linspace(0, self.DURATION, len(x_k))

                f_initial_guess = interp1d(t_interp, x_k, kind='cubic')
                new_initial_guess[k*self.NUM_NODES:(k+1)*self.NUM_NODES] = f_initial_guess(self.t_simu)

            initial_guess = new_initial_guess

        for n_start in range(N_start):

            time_4 = time.time()
            # initial_guess = np.zeros((NUM_STATES_TOT+NUM_INPUTS_TOT)*NUM_NODES)
            
            
            # Find the optimal solution.
            self.sol, self.info = self.problem.solve(initial_guess)
            

            # problem.plot_trajectories(sol)
            

            self.x_opt, self.T_opt = self.sol.reshape(-1, self.NUM_NODES)[:-self.NUM_INPUTS_TOT,:], self.sol.reshape(-1, self.NUM_NODES)[-self.NUM_INPUTS_TOT:,:]
            
            time_5 = time.time()
            
            self.solving_time = round(time_5 - time_4, 2)
            
            print(f'- START {n_start+1}/{N_start} SOLVED - {self.solving_time} sec')

            to_save = {
                'model_type' : self.model_type,
                'n_start' : n_start,
                'NUM_STATES' : self.NUM_STATES,
                'NUM_INPUTS' : self.NUM_INPUTS,
                'NUM_MODELS' : self.NUM_MODELS,
                'WEIGHT' : self.WEIGHT,
                'SPEED' : self.SPEED,
                'DURATION' : self.DURATION,
                'NUM_NODES'  : self.NUM_NODES,
                'INTERVAL_VALUE' : self.INTERVAL_VALUE,
                'known_trajectories' : self.known_trajectories,
                'x' : self.x,
                'r' : self.r,
                'p' : self.p,
                'bounds' : self.bounds,
                'obj_value' : self.problem.obj_value,
                'info' : self.info,
                'model_generation_time' : self.init_time,
                'prob_def_time' : self.prob_def_time,
                'solving_time' : self.solving_time,
                't_simu' : self.t_simu,
                'x_opt' : self.x_opt,
                'T_opt' : self.T_opt}
                
            save_results(to_save, path_results_folder)
            export_results_as_csv(self.t_simu, self.x_opt, self.T_opt, self.x, self.r, n_start, self.model_type, self.known_trajectories, path_results_folder)

            if visualization_flag == True:
              
                self.problem.plot_objective_value()
                self.problem.plot_constraint_violations(self.sol)
                
                
                for model_index in range(self.NUM_MODELS):
                            
                            
                    x_opt_model = self.x_opt[model_index*self.NUM_STATES:self.NUM_STATES*(model_index+1), :]
                    T_opt_model = self.T_opt[model_index*self.NUM_INPUTS:self.NUM_INPUTS*(model_index+1), :]
                    disturbance_model =  self.known_trajectories[list(self.known_trajectories.keys())[model_index]]
                    x_model = self.x[model_index*self.NUM_STATES:self.NUM_STATES*(model_index+1)]
                    r_model = self.r[model_index*self.NUM_INPUTS]
                    
                    plot_optimal_solution(self.t_simu, self.x_list, self.r_list, self.disturbance_list, n_start, model_index, np.vstack((x_opt_model, T_opt_model, disturbance_model)), path_results_folder)
            
                    animate_solution(self.t_simu, 
                                 x_opt_model, 
                                 T_opt_model, 
                                 disturbance_model, 
                                 self.bicycle_list[model_index], 
                                 x_model, 
                                 r_model,
                                 self.p,
                                 path_results_folder + f'animation_{model_index}')
        
      

