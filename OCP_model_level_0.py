class SOCP_problem:
    
    def __init__(self, NUM_MODELS, model_type):
        """
        Initialize the SOCP_problem class that can handle the optimization 
        process using Opty for different model type. The problem is solving for
        optimal control for multiple episodes at the same time. Gaussian noise 
        is introduced in each model.

        Parameters
        ----------
        NUM_MODELS : int
            Nomber of models to optimize at the same time 
            (ie. number of episodes).
        model_type : str
            type of model (i.e. name of model generated by the 'generate_model'
            function).

        Returns
        -------
        None.

        """
        
        
        # Generates N models and compile them
        time_0 = time.time()
        model_names = [f'm{k}' for k in range(NUM_MODELS)]
        
        print(f'PROBLEM INITIALIZED - {NUM_MODELS} models of type "{model_type}":')

        t_list, x_list, r_list, eoms_list, p_list, bicycle_list, disturbance_list = [],[],[],[],[],[],[]

        for model in model_names:
            t_model, x_model, r_model, eoms_model, p_model, bicycle_model, disturbance_model = generate_model(model)
            
            t_list.append(t_model)
            x_list.append(x_model)
            r_list.append(r_model)
            eoms_list.append(eoms_model)
            p_list.append(p_model)
            bicycle_list.append(bicycle_model)
            disturbance_list.append(disturbance_model)

        self.NUM_MODELS = NUM_MODELS
        self.t = t_list[0]
        self.x = x_list[0]
        self.r = r_list[0]
        self.eoms = eoms_list[0]
        self.p = p_list[0]
        self.disturbance = disturbance_list[0]
        self.bicycle_list = bicycle_list
        
        self.x_list = x_list
        self.r_list = r_list
        self.disturbance_list = disturbance_list
        
        if self.NUM_MODELS>1:
            for k in range(1, NUM_MODELS):
                self.x = self.x.col_join(x_list[k])
                self.r = self.r.col_join(r_list[k])
                self.eoms = self.eoms.col_join(eoms_list[k])
                self.disturbance = self.disturbance.col_join(disturbance_list[k])
        



        self.param, self.param_vals = zip(*self.p.items())




        self.NUM_STATES = len(x_list[0])
        self.NUM_INPUTS = len(r_list[0])
        self.NUM_KNOWN_TRAJ = 1
        
        self.NUM_STATES_TOT = NUM_MODELS*self.NUM_STATES
        self.NUM_INPUTS_TOT = NUM_MODELS*self.NUM_INPUTS
        time_1 = time.time()
        
        self.init_time = round(time_1 - time_0, 2)
        
        self.model_type = model_type

        
        print(f'-MODEL(S) INITIALIZED - { self.init_time} sec')
        


